## Using venv we can create virtual evnvironment using python

# Important command line codes

# python -m venv <name> # To Create a vitual environment

# To activate virtual environment
# cmd.exe

# C:\> <venv>\Scripts\activate.bat

# PowerShell

# PS C:\> <venv>\Scripts\Activate.ps1

# To deactivate 
# C:\> <venv>\Scripts\deactivate.bat

# To create list of all packages we have installed
# pip freeze > requirements.txt
# And at another end it can be installed
